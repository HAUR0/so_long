#include "libft.h"

int	pr_putstr(char *b, int cnt)
{
	int	i;

	i = 0;
	if (!b)
	{
		write(1, "(null)", 6);
		return (cnt + 6);
	}
	while (b[i])
	{
		write(1, &b[i], 1);
		cnt++;
		i++;
	}
	return (cnt);
}
